export const DEFAULT_TERMINAL_THEME_ID = 'nord';

export function getTerminalThemeById(id: string): TerminalThemePreset | undefined {
  return TERMINAL_THEME_PRESETS.find(preset => preset.id === id);
}

export function getDefaultTerminalTheme(): TerminalThemePreset {
  return getTerminalThemeById(DEFAULT_TERMINAL_THEME_ID) || TERMINAL_THEME_PRESETS[0];
}
